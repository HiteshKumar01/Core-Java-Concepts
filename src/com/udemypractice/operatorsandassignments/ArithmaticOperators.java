package operatorsandassignments;

public class ArithmaticOperators {
    public static void main(String[] args){
        float a=2;
        float b=3;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println(2*5+3-6*2/(5*4+2));
    }
}
