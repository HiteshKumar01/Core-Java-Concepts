package com.udemypractice.operatorsandassignments;

public class TernaryOperator {
    public static void main(String[] args){
        int a=20;
        int b=5;
        int min=(a<b)?a:b;
        System.out.println(min);

        int c=10;
        int d=20;
        int max=(c>d)?c:d;
        System.out.println(max);
    }
}

